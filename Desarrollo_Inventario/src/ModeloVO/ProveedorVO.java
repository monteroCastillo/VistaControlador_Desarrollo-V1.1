package ModeloVO;





public class ProveedorVO {
    
    String nombreProveedor, nit,direccioProveedor, telefonoProveedor, ciudad, email;

    public ProveedorVO() {
    
    }

    public String getNombreProveedor() {
        return nombreProveedor;
    }

    public String getNit() {
        return nit;
    }

    public String getDireccioProveedor() {
        return direccioProveedor;
    }

    public String getTelefonoProveedor() {
        return telefonoProveedor;
    }

    public String getCiudad() {
        return ciudad;
    }

    public String getEmail() {
        return email;
    }

    public void setNombreProveedor(String nombreProveedor) {
        this.nombreProveedor = nombreProveedor;
    }

    public void setNit(String nit) {
        this.nit = nit;
    }

    public void setDireccioProveedor(String direccioProveedor) {
        this.direccioProveedor = direccioProveedor;
    }

    public void setTelefonoProveedor(String telefonoProveedor) {
        this.telefonoProveedor = telefonoProveedor;
    }

    public void setCiudad(String ciudad) {
        this.ciudad = ciudad;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    
    
    
}
